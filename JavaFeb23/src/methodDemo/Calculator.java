package methodDemo;

public class Calculator {

	public int doAdd(int a, int b) {

		int c = a + b;
		return c;

	}

	public int doSub(int a, int b) {

		int c = a - b;
		return c;

	}

	public String concatStr(String a, String b) {

		String c = a + " " + b;
		return c;
	}

}
