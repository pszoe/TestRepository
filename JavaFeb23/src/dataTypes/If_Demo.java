public class If_Demo {

	public static void main(String[] args) {
		
		/*
		 * Syntax:
		 * if (boolean condition)
		 * {
		 * code to be executed
		 * }
		 */
		
		int age = 16;
		System.out.println("Testing of condition starts: ");
		
		if(age<18)
		{
			System.out.println("You are not eligible for marriage");
		}
		
		System.out.println("Testing of condition ends: ");

	}

}
